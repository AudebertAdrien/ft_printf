#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	s;

	s = 0;
	if (n < 0)
	{
		ft_putchar('-');
		s = n * -1;
	}
	else
		s = n;
	if (s > 9)
		ft_putnbr(s / 10);
	ft_putchar(s % 10 + 48);
}
